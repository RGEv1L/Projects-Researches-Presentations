//	Q-4.2
// Write four different C++ statements that each add 1 to integer variable x.

#include <iostream>
using namespace std;


int main()
{

int x=0;
	
	x++;    //Post-Increment
	++x;	//Pre-Increment
	x = x + 1;	//Increment using assignment
	cout<< x + 1;   //Increment inside cout statement
	
return 0;

}
